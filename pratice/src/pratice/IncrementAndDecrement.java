package pratice;

public class IncrementAndDecrement {
	
	public static void main(String[] args) {
		  
		int x=10;
		int y=20;
		int z=40;
		int p=60;
		x++;
		++y;
		z--;
		--p;
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(p);
		
	}

}
